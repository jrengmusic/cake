// CakeLieBanner renders the "The cake is a lie" banner with braille art
// Used when cake is launched outside a CMake project
func RenderCakeLieBanner(width int, height int, theme Theme) string {
	// Try to load and convert the SVG to braille
	brailleArt := loadCakeLieBraille(width, height)

	// Style the braille art
	artStyle := lipgloss.NewStyle().
		Foreground(lipgloss.Color(theme.DimmedTextColor))

	styledArt := artStyle.Render(brailleArt)

	// Center vertically and horizontally
	artLines := strings.Split(styledArt, "\n")
	artHeight := len(artLines)

	// Calculate vertical padding
	topPad := (height - artHeight) / 2
	if topPad < 0 {
		topPad = 0
	}
	bottomPad := height - artHeight - topPad
	if bottomPad < 0 {
		bottomPad = 0
	}

	var result strings.Builder

	// Top padding
	for i := 0; i < topPad; i++ {
		result.WriteString(strings.Repeat(" ", width))
		if i < topPad-1 || artHeight > 0 {
			result.WriteString("\n")
		}
	}

	// Art lines (centered horizontally)
	for i, line := range artLines {
		lineWidth := lipgloss.Width(line)
		leftPad := (width - lineWidth) / 2
		if leftPad < 0 {
			leftPad = 0
		}
		centeredLine := strings.Repeat(" ", leftPad) + line
		// Pad to full width
		finalWidth := lipgloss.Width(centeredLine)
		if finalWidth < width {
			centeredLine = centeredLine + strings.Repeat(" ", width-finalWidth)
		}
		result.WriteString(centeredLine)
		if i < len(artLines)-1 || bottomPad > 0 {
			result.WriteString("\n")
		}
	}

	// Bottom padding
	for i := 0; i < bottomPad; i++ {
		result.WriteString(strings.Repeat(" ", width))
		if i < bottomPad-1 {
			result.WriteString("\n")
		}
	}

	return result.String()
}

